package com.uts.babelfood;

public class Information {
    private int id_information;
    private String tgl_information;
    private String email;
    private String komentar;
    private String tanggapan;
    private String status;
    private String kategori;

    // Getters
    public int getId_information() {
        return id_information;
    }

    public String getTgl_information() {
        return tgl_information;
    }

    public String getEmail() {
        return email;
    }

    public String getKomentar() {
        return komentar;
    }

    public String getTanggapan() {
        return tanggapan;
    }

    public String getStatus() {
        return status;
    }

    public String getKategori() {
        return kategori;
    }
}
